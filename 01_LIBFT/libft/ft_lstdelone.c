#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	(del)(lst->content);
	free(lst);
}

// we are sending free function in ft_lstdelone. Del can be named however
// but it will do still the free stuff - na jméně nezáleží
