#include "ft_printf_utils/ft_printf_utils.h"

/*
 *   %c <-> 1
 *   %s <-> 2
 *   %p <-> 3
 *   %d <-> 4
 *   %i <-> 5
 *   %u <-> 6
 *   %x <-> 7
 *   %X <-> 8
 *   %% <-> 9
 */

int	ft_printf(const char *input, ...)
{
	va_list	args;
	int		tmp;
	int		count;

	va_start(args, input);
	tmp = -10;
	count = 0;
	while (*input != 0)
	{
		if (*input == '%')
			tmp = checkflag((char *)input);
		else
			tmp = -1;
		if (tmp != -1)
		{
			count += printflag(args, tmp);
			input++;
		}
		else
			printhelper((char *)input, &count);
		input++;
	}
	va_end(args);
	return (count);
}

