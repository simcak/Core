#include "ft_printf.h"

int	ft_numlen(int n)
{
	int	count;

	count = 0;
	if (n == 0)
	{
		return (1);
	}
	else if (n < 0)
	{
		count++;
		n = -n;
	}
	while (n > 0)
	{
		count++;
		n /= 10;
	}
	return (count);
}
